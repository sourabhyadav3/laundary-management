export const getFlatTranslationMap = () => ({
  ...buildFromNestedTranslations(),
  ...pageStrings,
});

export const translateString = (text, lang) => {
  if (!text || lang !== 'ar') return text;
  const map = getFlatTranslationMap();
  const trimmed = String(text).trim();
  return map[trimmed] || text;
};
